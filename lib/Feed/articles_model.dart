class Post {
  final int id;
  final String date;
  final String title;
  final String desc;
  final String content;
  final String link;
  final String imageUrl;

  Post({
    required this.id,
    required this.date,
    required this.title,
    required this.desc,
    required this.content,
    required this.link,
    required this.imageUrl,
  });
}